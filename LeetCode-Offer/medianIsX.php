/**
 * 题目描述
 * 小M给你一个长度为n的数组，我们定义median数为该数组从小到大排序后，下标为(n-1)/2的数字
 * 下标从0开始，(n-1)/2表示整数除法，即向下取整
 * 现在我们已经得到了一个初始的数组，我们希望这个数组的median数是一个给定数字x
 * 所以我们需要加入一些数到数组中从而完成我们的目标
 * 数组中的元素可以重复，请问，最少需要加入多少个数字才能达成这个目标
 *
 * example1:
 * input:  3 2
 *         2 3 4
 * output: 1
 * 样例一中，加入1，得到1 2 3 4，那么median数的下标为(4 - 1)/2 = 1, median数为2。加一个数字就可以了。
 *
 * example2:
 * input:  3 4
 *         1 2 3
 * output: 4
 * 样例二中，加入4 5 6 7，得到1 2 3 4 5 6 7，median数为4。最少加4个数字。
 * @link https://www.nowcoder.com/practice/47232470945644458213ddd07580e121
 */

function medianIsX($count, $x, $array)
{
    $number = 0;
    $left = $right = $x;
    for (;;) {
        sort($array);
        switch ($array[floor(($count - 1) >> 1)] <=> $x) {
            case 1:
                $array[] = $left > 1 ? $left-- : $left;
                break;
            case -1:
                $array[] =  $right++;
                break;
            case 0:
                return $number;
        }
        $count += 1;
        $number += 1;
    }
}

function medianIsX1($count, $x, $array)
{
    $middle = floor(($count - 1) >> 1);
    $left = $right = $number = 0;
    foreach ($array as $value) {
        if ($value == $x)
            $number++;
        if ($value < $x)
            $left++;
        if ($value > $x)
            $right++;
    }

    if ($left <= $middle) {
        if ($right < $number - $middle)
            return 0;
        else
            return $right - $left - $number;
    } else {
        return $left + 1 - $right - $number;
    }
}

